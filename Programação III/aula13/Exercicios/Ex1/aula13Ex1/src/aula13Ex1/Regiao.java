package aula13Ex1;

public class Regiao {
	private String nome;
	private int populacao;
	
	public void setNome(String name) {
		this.nome = name;
	}
	
	public void setPopulacao(int pop) {
		this.populacao = pop;
	}
	
	public String getNome() {
		return this.nome;
	}
	
	public int getPopulacao() {
		return this.populacao;
	}
}
